/**
  * @brief  this function handles trng handler.
  * @param  none
  * @retval none
  */
void TRNG_IRQHandler(void)
{
  if(trng_interrupt_flag_get(TRNG_DTRDY_FLAG) == SET)
  {
    random_number = trng_value_get();
  }
  if(trng_interrupt_flag_get(TRNG_CEFIS_FLAG) == SET)
  {
    trng_flag_clear(TRNG_CEFIS_FLAG);
  }
  if(trng_interrupt_flag_get(TRNG_ESFIS_FLAG) == SET)
  {
    trng_flag_clear(TRNG_ESFIS_FLAG);
  }
}
